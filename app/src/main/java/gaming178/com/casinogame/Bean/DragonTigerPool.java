package gaming178.com.casinogame.Bean;

public class DragonTigerPool {
	private int dragon;
	private int tiger;
	private int tie;
	public void Init()
	{
		dragon = 0;
		tiger = 0;
		tie = 0;
	}
	public int getDragon() {
		return dragon;
	}
	public void setDragon(int dragon) {
		this.dragon = dragon;
	}
	public int getTiger() {
		return tiger;
	}
	public void setTiger(int tiger) {
		this.tiger = tiger;
	}
	public int getTie() {
		return tie;
	}
	public void setTie(int tie) {
		this.tie = tie;
	}

}
