package gaming178.com.casinogame.Bean;

public class DragonTigerPoker {
	private int dragon;
	private int tiger;
	
	public DragonTigerPoker() {
		super();
		dragon = 0;
		tiger = 0;
	}
	public void Init()
	{
		dragon = 0;
		tiger = 0;
	}
	public int getDragon() {
		return dragon;
	}
	public void setDragon(int dragon) {
		this.dragon = dragon;
	}
	public int getTiger() {
		return tiger;
	}
	public void setTiger(int tiger) {
		this.tiger = tiger;
	}

}
